void C滚动条Dlg::Draw(void)
{
	CWnd *pWnd=GetDlgItem(IDC_DRAW);
	CDC *pDC=pWnd->GetDC();
	CBrush drawBrush;
	drawBrush.CreateSolidBrush(m_RValue);
	CBrush *pOldBrush=pDC->SelectObject(&drawBrush);
	CRect rcClient;
	pWnd->GetClientRect(rcClient);
	pDC->Rectangle(rcClient);
	pDC->SelectObject(pOldBrush);
}


void C滚动条Dlg::OnHScroll(UINT nSBCode, UINT nPos, CScrollBar* pScrollBar)
{
	// TODO: 在此添加消息处理程序代码和/或调用默认值
	int nID=pScrollBar->GetDlgCtrlID();
	if(nID==IDC_SCROLLBAR1)
	{
		switch (nSBCode)
		{
		case SB_LINELEFT://单击滚动条左箭头
			m_RValue--;break;
		case SB_LINERIGHT:
			m_RValue++;break;
		case SB_PAGELEFT:
			m_RValue-=10;break;
		case SB_PAGERIGHT:
			m_RValue+=10;break;
		case SB_THUMBTRACK:
			m_RValue=nPos;break;
		default:
			break;
		}
		if(m_RValue<0)m_RValue=0;
		if(m_RValue>255)m_RValue=255;
		m_Scroll.SetScrollPos(m_RValue);
	}
	UpdateData(false);
	Draw();
	CDialogEx::OnHScroll(nSBCode, nPos, pScrollBar);
}
